def stringSplitter(string):
    '''
    A function named stringSplitter which turns a string to a list.

    Parameter: string, a text which is of type string.

    Return: a list which contains all the elements in the string.
    
    @author: Babatunde Koiki
    Created on 2020-03-28
    '''
    return [j for j in string]  # Returns each element in a string as a list.

