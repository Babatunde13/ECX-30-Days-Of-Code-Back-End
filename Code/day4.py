def intersectionEye(list1, list2):
    ''''
    A function named intersectionEye which checks for unique elements in two lists

    Parameter: Two lists, list1 and list2

    Returns:  The unique element(s) in both lists as an array.
    
    @author: Babatunde Koiki
    Created on 2020-03-28
    '''
    return [x for x in list1 if x in list2] 

